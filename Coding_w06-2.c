#include <stdio.h>          // นำเข้า libary Stdio.h (Starndard Input Output Header) มาใช้งานฟังก์ชั่น input,output

int main() {                // สร้างฟังก์ชั่นหลักของโปรแกรม เพื่อเป็นจุดเริ่มต้นการทำงานเมื่อรันโปรแกรม
    // ─────────────────────────────
    // ตอนที่ 1: การดำเนินการกับตัวแปรประเภท int
    // ─────────────────────────────
    int i = 1, j = 2, k;    // กำหนดตัวแปรจำนวนเต็ม โดยที่ i มีค่าเป็น 1 และ j มีค่าเป็น 2 และ k ยังไม่ทราบค่า
    printf("ตอนที่ 1:\n");

        // บรรทัดที่ 1: กำหนดค่าเริ่มต้น
        printf("บรรทัดที่ 1: กำหนดค่าเริ่มต้น → i = %d, j = %d\n", i, j);

        // บรรทัดที่ 2: คำนวณ k = i + j
        k = i + j;
        printf("บรรทัดที่ 2: k = i + j = %d + %d = %d\n", i, j, k);

        // บรรทัดที่ 3: คำนวณ i = i + (k * j)
        i = i + (k * j);
        printf("บรรทัดที่ 3: i = i + (k * j) = %d + (%d * %d) = %d\n", i - (k * j), k, j, i);

        // บรรทัดที่ 4: คำนวณ j = i / 2
        j = i / 2;
        printf("บรรทัดที่ 4: j = i / 2 = %d / 2 = %d\n", i, j);

        // บรรทัดที่ 5: คำนวณ k = j % 2
        k = j % 2;
        printf("บรรทัดที่ 5: k = j %% 2 = %d %% 2 = %d\n", j, k);

        // บรรทัดที่ 6: คำนวณ i = (j + k) * 3
        i = (j + k) * 3;
        printf("บรรทัดที่ 6: i = (j + k) * 3 = (%d + %d) * 3 = %d\n", j, k, i);

        // บรรทัดที่ 7: แสดงผลลัพธ์สุดท้าย
        printf("บรรทัดที่ 7: ผลลัพธ์สุดท้าย → i = %d, j = %d, k = %d\n\n", i, j, k);


    // ─────────────────────────────
    // ตอนที่ 2: การดำเนินการกับตัวแปรประเภท double
    // ─────────────────────────────
    double x = 1.0, y = 2.0, z; // กำหนดตัวแปรทศนิยม โดยที่ x มีค่าเป็น 1.0 และ y มีค่าเป็น 2.0 และ z ยังไม่ทราบค่า
    printf("ตอนที่ 2:\n");

        // บรรทัดที่ 1: กำหนดค่าเริ่มต้น
        printf("บรรทัดที่ 1: กำหนดค่าเริ่มต้น → x = %.1f, y = %.1f\n", x, y);

        // บรรทัดที่ 2: คำนวณ x = y + 5.0
        x = y + 5.0;
        printf("บรรทัดที่ 2: x = y + 5.0 = %.1f + 5.0 = %.1f\n", y, x);

        // บรรทัดที่ 3: คำนวณ y = x / 2.0
        y = x / 2.0;
        printf("บรรทัดที่ 3: y = x / 2.0 = %.1f / 2.0 = %.1f\n", x, y);

        // บรรทัดที่ 4: คำนวณ y = (x * 3.0) + 4.0
        y = (x * 3.0) + 4.0;
        printf("บรรทัดที่ 4: y = (x * 3.0) + 4.0 = (%.1f * 3.0) + 4.0 = %.1f\n", x, y);

        // บรรทัดที่ 5: คำนวณ x = -0.5 - y
        x = -0.5 - y;
        printf("บรรทัดที่ 5: x = -0.5 - y = -0.5 - %.1f = %.1f\n", y, x);

        // บรรทัดที่ 6: คำนวณ z = x + y
        z = x + y;
        printf("บรรทัดที่ 6: z = x + y = %.1f + %.1f = %.1f\n", x, y, z);

        // บรรทัดที่ 7: แสดงผลลัพธ์สุดท้าย
        printf("บรรทัดที่ 7: ผลลัพธ์สุดท้าย → x = %.1f, y = %.1f, z = %.1f\n", x, y, z);

    return 0;   // ทำการ reset ค่าของ code เพื่อสิ้นสุดกระบวนการ
}
